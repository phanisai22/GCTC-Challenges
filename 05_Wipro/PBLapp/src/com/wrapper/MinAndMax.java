package com.wrapper;

public class MinAndMax {

    public static void main(String[] args) {
        System.out.println("Integer range:\n\tmin: " + Integer.MIN_VALUE + "\n\tmax: " + Integer.MAX_VALUE);
        System.out.println("Double range:\n\tmin: " + Double.MIN_VALUE + "\n\tmax: " + Double.MAX_VALUE);
        System.out.println("Long range:\n\tmin: " + Long.MIN_VALUE + "\n\tmax: " + Long.MAX_VALUE);
        System.out.println("Short range:\n\tmin: " + Short.MIN_VALUE + "\n\tmax: " + Short.MAX_VALUE);
        System.out.println("Byte range:\n\tmin: " + Byte.MIN_VALUE + "\n\tmax: " + Byte.MAX_VALUE);
        System.out.println("Float range:\n\tmin: " + Float.MIN_VALUE + "\n\tmax: " + Float.MAX_VALUE);
    }
}
